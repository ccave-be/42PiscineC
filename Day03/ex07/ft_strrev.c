int		ft_putchar(char c);

char	*ft_strrev(char *str)
{
	int		n;
	int		i;
	char	temp;

	n = 0;
	while (str[n] != '\0')
	{
		n++;
	}
	i = 0;
	while (i < n / 2)
	{
		temp = str[i];
		str[i] = str[n - i - 1];
		str[n - i - 1] = temp;
		i++;
	}
	return (str);
}
