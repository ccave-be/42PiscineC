int		ft_plus(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int		ft_less(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int		ft_multiplied(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int		ft_divided(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int		ft_modulo(int nb1, int nb2)
{
	return (nb1 % nb2);
}
