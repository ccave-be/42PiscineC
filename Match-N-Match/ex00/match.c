int		match(char *s1, char *s2)
{
	if (!*s1 && *s2)
		return ((*s2 == '*') ? match(s1, ++s2) : 0);
	else if ((*s2 == '*' || *s1 == *s2) && *s1 && *s2)
		return ((*s2 == '*') ? match(++s1, s2) || match(--s1, ++s2) :
		match(++s1, ++s2));
	return ((!*s1 && !*s2) ? 1 : 0);
}
